-- 목적: 초대 보상 유실 자가 복구. 계정 생성 후 보상 적립 전에 실패·크래시가 나면 계정만 남고 보상은 없는데,
--       초대자 관계를 어디에도 안 남기면 다음 로그인이 이를 복구할 수 없다(가입 보상과 달리 request의 코드에 의존).
--       이 컬럼을 계정 생성 트랜잭션에 함께 커밋해, 매 로그인 멱등 재적립으로 유실을 복구한다.
-- nullable: 초대 없이 가입했거나 코드가 무효면 NULL. 값이 있으면 그 사용자가 이 회원을 초대했다는 뜻이다.
